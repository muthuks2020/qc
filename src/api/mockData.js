// Get inspection queue items
export const getMockInspectionQueue = (filters = {}) => {
  let items = [...mockInspectionQueue];
  
  if (filters.status) {
    items = items.filter(item => item.status === filters.status);
  }
  
  if (filters.partCode) {
    items = items.filter(item => item.partCode === filters.partCode);
  }
  
  if (filters.vendor) {
    items = items.filter(item => 
      item.vendor.toLowerCase().includes(filters.vendor.toLowerCase())
    );
  }
  
  return items;
};

